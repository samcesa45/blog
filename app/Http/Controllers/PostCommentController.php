    {
        //technique
        //we are finding the post and adding a comment to the particular post
        // $body = $request->input('body');
    
        request()->validate([
            'body'=>'required'
        ]);
      
        $post->comments()->create([
            'user_id'=> auth()->user()->id,
            'body'=>request('body')
        ]);

        return back();
    }

